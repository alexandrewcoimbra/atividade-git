def soma(n1: int, n2: int) -> int:
    """
    Soma dois números
    Args:
    n1: primeiro número;
    n2: segundo numero;

    Returns:
    A soma de dois números.
    """
    return n1 + n2


def subtracao(n1: int, n2: int) -> int:
    """
    Subtrai dois números
    Args:
    n1: primeiro número;
    n2: segundo numero;

    Returns:
    A subtração de dois números.
    """
    return n1 - n2

